public class Ques_18 {
    public static void main(String[] args) {
        // max of 2 number using max method
         int a = 3;
         int b = 6;

         int max_num = Integer.max(a, b);         
         System.out.println(max_num);

        // min of 2 number using min method

        int min_num = Integer.min(a, b);
        System.out.println(min_num);

        // sum of two number

        int sum_num = Integer.sum(a, b);
        System.out.println(sum_num);

    }
    
}
